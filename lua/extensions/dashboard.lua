return {
    "nvimdev/dashboard-nvim",
    lazy = false,
    opts = function()
        logo = string.rep("\n", 8) .. logo .. "\n\n"

        local opts = {
            theme = "doom",
            hide = { statusline = false },
            config = {
                header = vim.split(logo, "\n"),
                center = {
                    { icon = " ", desc = " Find File", action = "Telescope find_files", key = "f" },
                    { icon = " ", desc = " New File", action = "ene | startinsert", key = "n" },
                    { icon = " ", desc = " Recent Files", action = "Telescope oldfiles", key = "r" },
                    { icon = " ", desc = " Projects", action = "Workspaces", key = "p" },
                    { icon = " ", desc = " Themes", action = "Telescope themes", key = "t" },
                    { icon = " ", desc = " Config", action = 'Telescope find_files cwd=~/.config/nvim', key = "c" },
                    { icon = " ", desc = " Restore Session", action = 'lua require("persistence").load()', key = "s" },
                    { icon = "󰒲 ", desc = " Lazy", action = "Lazy", key = "l" },
                    {
                        icon = " ",
                        desc = " Quit",
                        action = function()
                            vim.api.nvim_input("<cmd>qa<cr>")
                        end,
                        key = "q"
                    },
                }
            }
        }

        for _, button in ipairs(opts.config.center) do
            button.desc = button.desc .. string.rep(" ", 43 - #button.desc)
            button.key_format = "  %s"
        end

        if vim.o.filetype == "lazy" then
            vim.api.nvim_create_autocmd("WinClosed", {
                pattern = tostring(vim.api.nvim_get_current_win()),
                once = true,
                callback = function()
                    vim.schedule(function()
                        vim.api.nvim_exec_autocmds("UIEnter", { group = "dashboard" })
                    end)
                end
            })
        end

        return opts
    end
}
